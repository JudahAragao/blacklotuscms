/**
 * Generates the static theme registry and the isolated stylesheet consumed by
 * the single Next.js build. Themes are source-controlled folders, never
 * runtime uploads.
 */
import fs from 'node:fs/promises';
import path from 'node:path';

const root = process.cwd();
const themesDir = path.join(root, 'themes');
const generatedDir = path.join(root, 'src', 'generated');

/** Semantic tokens that Tailwind v4 compiles at build time. */
const tokenVariables = new Set([
  '--color-background', '--color-foreground', '--color-primary', '--color-primary-foreground',
  '--color-secondary', '--color-secondary-foreground', '--color-muted', '--color-muted-foreground',
  '--color-card', '--color-card-foreground', '--color-border', '--color-input', '--color-ring',
  '--color-destructive', '--color-destructive-foreground',
  '--color-accent', '--color-accent-foreground',
  '--font-sans', '--font-display', '--font-mono',
  '--radius-sm', '--radius-md', '--radius-lg', '--radius-xl',
]);

const validId = (v) => /^[a-z0-9]+(?:-[a-z0-9]+)*$/.test(v);

function namespaceAnimations(css, id) {
  for (const name of [...css.matchAll(/@keyframes\s+([\w-]+)/g)].map((m) => m[1])) {
    css = css.replace(new RegExp(`\\b${name}\\b`, 'g'), `bl-${id}-${name}`);
  }
  return css;
}

function validateCss(css, id) {
  if (/(^|[,{\s])(html|body)(?=\s|[.{:#>+~])/m.test(css)) {
    throw new Error(`Theme "${id}" uses html/body. Use :root or a local selector.`);
  }
  const declared = new Set([...css.matchAll(/(--[\w-]+)\s*:/g)].map((m) => m[1]));
  const used = [...new Set([...css.matchAll(/var\(\s*(--[\w-]+)/g)].map((m) => m[1]))];
  const missing = used.filter((v) => !declared.has(v) && !tokenVariables.has(v) && !v.startsWith('--tw-'));
  if (missing.length) {
    throw new Error(`Theme "${id}" references undeclared CSS variables: ${missing.join(', ')}`);
  }
}

/* ── Discover themes ──────────────────────────────────────── */

const entries = await fs.readdir(themesDir, { withFileTypes: true });
const themes = [];

for (const entry of entries.filter((e) => e.isDirectory()).sort((a, b) => a.name.localeCompare(b.name))) {
  const id = entry.name;
  if (!validId(id)) throw new Error(`Invalid theme id "${id}". Use lowercase kebab-case.`);

  const directory = path.join(themesDir, id);
  const manifestPath = path.join(directory, 'theme.json');
  if (!(await fs.stat(manifestPath).catch(() => null))) continue;

  const entrypoint = (await fs.stat(path.join(directory, 'theme.ts')).catch(() => null))
    ? 'theme.ts'
    : 'layouts/index.ts';

  const [manifestRaw, css] = await Promise.all([
    fs.readFile(manifestPath, 'utf8'),
    fs.readFile(path.join(directory, 'style.css'), 'utf8'),
  ]);

  const manifest = JSON.parse(manifestRaw);
  if (!manifest.name || !manifest.version) {
    throw new Error(`Theme "${id}" needs name and version in theme.json.`);
  }

  /* themeApiVersion validation */
  if (manifest.themeApiVersion !== undefined && manifest.themeApiVersion !== 1) {
    throw new Error(
      `Theme "${id}" declares themeApiVersion ${manifest.themeApiVersion}, but only version 1 is supported.`
    );
  }

  validateCss(css, id);
  themes.push({
    id,
    entrypoint,
    css: namespaceAnimations(css.replace(/:root\b/g, ':scope'), id),
  });
}

if (!themes.some((t) => t.id === 'default')) throw new Error('The default theme is required.');

/* ── Write generated files ────────────────────────────────── */

await fs.mkdir(generatedDir, { recursive: true });

const imports = themes
  .map((t, i) => `import * as Theme${i} from '../../themes/${t.id}/${t.entrypoint.replace(/\.ts$/, '')}';`)
  .join('\n');

const registry = [
  '/* This file is generated by scripts/generate-theme-registry.mjs. Do not edit. */',
  "import type { ComponentType } from 'react';",
  imports,
  '',
  'export type ThemeLayouts = Record<string, ComponentType<any>>;',
  'export const themeRegistry: Record<string, ThemeLayouts> = {',
  ...themes.map((t, i) => `  ${JSON.stringify(t.id)}: Theme${i} as ThemeLayouts,`),
  '};',
  `export const bundledThemeIds = ${JSON.stringify(themes.map((t) => t.id))} as const;`,
  '',
].join('\n');

const scopedStyles = themes
  .map((t) => `\n@scope ([data-bl-theme="${t.id}"]) {\n${t.css}\n}\n`)
  .join('');

const fallbackStyles = themes
  .map((t) => {
    // Replace .blacklotuscms-theme with the scoped selector so CSS variables
    // are applied directly to the wrapper element, not as descendants.
    const scoped = t.css.replace(/\.blacklotuscms-theme\b/g, `.blacklotuscms-theme[data-bl-theme="${t.id}"]`);
    return `\n${scoped}\n`;
  })
  .join('');

const styles = [
  '/* Generated. Theme CSS is isolated by its active root. */',
  '',
  '/* ── Class-based isolation (all browsers) ── */',
  '/* Provides reliable isolation via .blacklotuscms-theme[data-bl-theme] selector. */',
  '/* Active in Chrome (with @scope), Firefox, Safari, and older browsers. */',
  fallbackStyles,
  '',
  '/* ── Native @scope isolation (Chrome 118+) ── */',
  '/* When supported, provides true CSS scope shadow-dom-like isolation. */',
  '/* Coexists with the class-based layer; Chrome picks the more specific @scope. */',
  scopedStyles,
  '',
].join('\n');

await Promise.all([
  fs.writeFile(path.join(generatedDir, 'theme-registry.ts'), registry),
  fs.writeFile(path.join(generatedDir, 'theme-styles.css'), styles),
]);

console.log(`Generated ${themes.length} bundled theme(s): ${themes.map((t) => t.id).join(', ')}`);
