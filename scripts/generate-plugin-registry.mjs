/**
 * Generates the static plugin registry consumed by the Next.js build.
 * Compiled plugins live in plugins/ and are compiled alongside the CMS.
 * Each plugin has a plugin.json manifest and an index.ts entry point.
 */
import fs from 'node:fs/promises';
import path from 'node:path';

const root = process.cwd();
const pluginsDir = path.join(root, 'plugins');
const generatedDir = path.join(root, 'src', 'generated');

const validId = (v) => /^[a-z0-9]+(?:-[a-z0-9]+)*$/.test(v);

/* ── Discover plugins ────────────────────────────────────── */

let entries;
try {
  entries = await fs.readdir(pluginsDir, { withFileTypes: true });
} catch {
  // plugins/ directory doesn't exist — generate empty registry
  entries = [];
}

const plugins = [];

for (const entry of entries.filter((e) => e.isDirectory()).sort((a, b) => a.name.localeCompare(b.name))) {
  const id = entry.name;
  if (!validId(id)) {
    console.warn(`Skipping invalid plugin id "${id}". Use lowercase kebab-case.`);
    continue;
  }

  const directory = path.join(pluginsDir, id);
  const manifestPath = path.join(directory, 'plugin.json');

  if (!(await fs.stat(manifestPath).catch(() => null))) {
    console.warn(`Skipping "${id}": no plugin.json found.`);
    continue;
  }

  const manifestRaw = await fs.readFile(manifestPath, 'utf8');
  const manifest = JSON.parse(manifestRaw);

  if (!manifest.name || !manifest.version) {
    throw new Error(`Plugin "${id}" needs name and version in plugin.json.`);
  }

  // Detect entry point (index.ts or index.js)
  const hasTs = await fs.stat(path.join(directory, 'index.ts')).catch(() => null);
  const hasJs = await fs.stat(path.join(directory, 'index.js')).catch(() => null);

  if (!hasTs && !hasJs) {
    throw new Error(`Plugin "${id}" needs an index.ts or index.js entry point.`);
  }

  const entrypoint = hasTs ? 'index.ts' : 'index.js';

  plugins.push({
    id,
    manifest,
    entrypoint,
  });
}

/* ── Write generated file ─────────────────────────────────── */

await fs.mkdir(generatedDir, { recursive: true });

const imports = plugins
  .map((p, i) => `import * as Plugin${i} from '../../plugins/${p.id}/${p.entrypoint.replace(/\.ts$/, '').replace(/\.js$/, '')}';`)
  .join('\n');

const registry = [
  '/* This file is generated by scripts/generate-plugin-registry.mjs. Do not edit. */',
  '',
  'export interface CompiledPluginManifest {',
  '  name: string;',
  '  version: string;',
  '  description?: string;',
  '  permissions: string[];',
  '}',
  '',
  'export interface CompiledPluginEntry {',
  '  init: (bridge: any) => Promise<any> | any;',
  '  manifest: CompiledPluginManifest;',
  '}',
  '',
  imports,
  '',
  'export const pluginRegistry: Record<string, CompiledPluginEntry> = {',
  ...plugins.map((p, i) => [
    `  ${JSON.stringify(p.id)}: {`,
    `    init: (Plugin${i} as any).default,`,
    `    manifest: ${JSON.stringify(p.manifest)},`,
    `  },`,
  ].join('\n')),
  '};',
  '',
  `export const compiledPluginNames = ${JSON.stringify(plugins.map((p) => p.id))} as const;`,
  '',
].join('\n');

await fs.writeFile(path.join(generatedDir, 'plugin-registry.ts'), registry);

console.log(`Generated ${plugins.length} compiled plugin(s): ${plugins.map((p) => p.id).join(', ') || '(none)'}`);
